# Write a Python program that demonstrates the use of logical operators (and, or, not) with boolean values to simulate real-world scenarios.
# Logical AND operator
x = 5
y = 10
z = 15

if x < y and y < z:
    print("x is less than y and y is less than z")

# Logical OR operator
age = 25

if age < 18 or age >= 65:
    print("You are either too young or too old to work")

# Logical NOT operator
Class = True

if not Class:
    print("Now, There is no class.")
else:
    print("Go to your respective classes.")
