# Write a Python program that converts an integer to a float and vice versa. Display the data type before and after conversion.

#Converting Int to float
num1=int(input("Enter an Integer number: "))
print("The entered integer is :", num1)
print("Data Type before conversion= ", type(num1))
num2= float(num1)
print("/nThe converted Float Number is : ", num2)
print("Data Type after conversion = ", type(num2))

#Converting float to Int
